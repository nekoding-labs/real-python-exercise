# Arithmetic

a = 10 + 20
b = 20 / 5.0
c = 20 - 10
d = -4 - - 5

print(a, b, c, d)

e = 10 // 3  # integer division (return integer type)
f = 10 / 3  # normal division (return float type)

print(e, f)

g = round(2.5, 0)
h = round(2.45, 1)

print(g, h)

i = abs(-10)
j = abs(20)

print(i, j)

k = pow(2, 4)
l = pow(2, 2)
m = pow(2, -2)

print(k, l, m)

print(m.is_integer())
